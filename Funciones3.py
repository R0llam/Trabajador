def Triangulo_Area(Base,Altura):
	T=(Base*Altura)/2
	return T
def Rectangulo_Area(Largo,Ancho):
	RA=Largo*Ancho
	return RA
def Rectangulo_Perimetro(Largo,Ancho):
	RP=(2*Largo)+(2*Ancho)
	return RP
def Cuadrado_Area(Lado):
	CA=Lado*Lado
	return CA
def Cuadrado_Perimetro(Lado):
	CP=Lado*4
	return CP